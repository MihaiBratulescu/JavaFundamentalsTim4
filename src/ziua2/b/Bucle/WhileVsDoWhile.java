package ziua2.b.Bucle;

public class WhileVsDoWhile
{
    public static void main(String args[])
    {
        int nr = 0;

        while (nr == 1)
        {
            System.out.println("while loop");
        }

        do
        {
            System.out.println("do-while loop");
        }while (nr == 1);
    }
}
