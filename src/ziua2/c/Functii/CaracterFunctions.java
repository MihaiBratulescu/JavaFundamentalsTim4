package ziua2.c.Functii;

public class CaracterFunctions
{
    public static void main(String args[])
    {
        System.out.println(Character.toUpperCase('g'));
        System.out.println(Character.toLowerCase('g'));
        System.out.println(Character.isAlphabetic('g'));
        System.out.println(Character.isUpperCase('g'));
        System.out.println(Character.isDigit('6'));
        System.out.println(Character.isLetter(']'));
    }
}
