package ziua1.c.Operatori;

public class InstructiuneaIf
{
    public static void function()
    {
        boolean booleanExpression = true;

        //Simple if
        if (booleanExpression)
        {
            // statement or block of code
        }

        //if-else
        if (booleanExpression)
        {
            // statement or block of code
        }
        else
        {
            // statement or block of code
        }

        //lant de conditii
        if (booleanExpression)
        {
            // statement or block of code
        }
        else if (booleanExpression)
        {
            // statement or block of code
        }
        else
        {
            // statement or block of code
        }
    }
}
