package ziua1.c.Operatori;

public class OperatoriRelationali
{
    public static void main(String args[])
    {
        int a = 3, b = 4;

        System.out.println(a < b);
        System.out.println(a > b);

        System.out.println(a <= b);
        System.out.println(a >= b);

        //equals
        System.out.println(a == b);
        //not-equals (! negare)
        System.out.println(a != b);
    }
}
