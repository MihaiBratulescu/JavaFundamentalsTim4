public class Main
{
    public static void main(String args[])
    {
        System.out.println("Hello World!");

        int numar;

        numar = 012556;
        System.out.println(numar);

        numar = 0xB32FC08;
        System.out.println(numar);

        numar = 0b11010111;
        System.out.println(numar);

        float numar2;

        numar2 = 213.34f;
        System.out.println(numar2);

        double numar3;

        numar3 = 32.11;
        System.out.println(numar3);

        numar3 = 0.3221e+2;
        System.out.println(numar3);

        char caracter;

        caracter = 2123;
        System.out.println(caracter);

        //backslash \ caracter de scapare
        caracter = '\'';
        System.out.println(caracter);

        caracter = '\\';
        System.out.println(caracter);

        caracter = '\uA7BC';
        System.out.println(caracter);

        boolean b = true;
        System.out.println(b);

        String s = "abcedar";
        System.out.println(s);

        System.out.println("Hello " + "World");

        int x = 3;
        System.out.println(x);
    }
}
